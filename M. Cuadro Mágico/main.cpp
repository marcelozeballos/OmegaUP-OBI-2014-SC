#include <cstdio>
using namespace std;

int main()
{
    int t[3][3];

    for(int i = 0; i < 3; i++)
        for(int j = 0; j < 3; j++)
            scanf("%d", &t[i][j]);

    if((t[0][0]+t[0][1]+t[0][2] == 15)&&(t[1][0]+t[1][1]+t[1][2] == 15)&&
       (t[2][0]+t[2][1]+t[2][2] == 15)&&(t[0][0]+t[1][0]+t[2][0] == 15)&&
       (t[0][1]+t[1][1]+t[2][1] == 15)&&(t[0][2]+t[1][2]+t[2][2] == 15)&&
       (t[0][0]+t[1][1]+t[2][2] == 15)&&(t[0][2]+t[1][1]+t[2][0] == 15))
            printf("SI\n");
    else
        printf("NO\n");
}
